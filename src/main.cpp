struct Command {
  const char *name;
  const uint16_t *data;
  uint16_t len;
};

#define CMD(n) {#n, kIr_##n, sizeof(kIr_##n) / sizeof(kIr_##n[0])}
const Command kCommands[] = {
  CMD(POWER), CMD(PLAY), CMD(STOP), CMD(REW),
  CMD(FF),    CMD(EJECT), CMD(PAUSE),
};
const size_t kNumCommands = sizeof(kCommands) / sizeof(kCommands[0]);

void printHelp() {
  Serial.print("commands: ");
  for (size_t i = 0; i < kNumCommands; i++) {
    Serial.print(kCommands[i].name);
    if (i + 1 < kNumCommands) Serial.print(", ");
  }
  Serial.println();
}

void dispatch(String cmd) {
  cmd.trim();
  if (cmd.length() == 0) return;
  cmd.toUpperCase();

  if (cmd == "?" || cmd == "HELP") { printHelp(); return; }

  for (size_t i = 0; i < kNumCommands; i++) {
    if (cmd == kCommands[i].name) {
      Serial.print("send ");
      Serial.println(kCommands[i].name);
      irsend.sendPronto(const_cast<uint16_t*>(kCommands[i].data), kCommands[i].len);
      return;
    }
  }
  Serial.print("unknown: ");
  Serial.println(cmd);
  printHelp();
}

void setup() {
  Serial.begin(115200);
  delay(200);
  irsend.begin();
  Serial.println();
  Serial.println("Panasonic PV-V4525S VCR remote. Type a command + Enter.");
  printHelp();
}

void loop() {
  if (Serial.available()) {
    String line = Serial.readStringUntil('\n');
    dispatch(line);
  }
}
